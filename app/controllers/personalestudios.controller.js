exports.getRecord = async(req, res) => {

    Personalestudios.findOne({
            where: {
                id: req.body.id
            }
        })
        .then(personalestudios => {
            if (!personalestudios) {
                return res.status(404).send({ message: "personalestudios Not found." });
            }

            res.status(200).send(personalestudios);
        })
        .catch(err => {
            res.status(500).send({ message: err.message });
        });
}

